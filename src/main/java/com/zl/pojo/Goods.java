package com.zl.pojo;


public class Goods {

  private Double gid;
  private String gname;
  private Double price;
  private Double traid;


  public Double getGid() {
    return gid;
  }

  public void setGid(Double gid) {
    this.gid = gid;
  }


  public String getGname() {
    return gname;
  }

  public void setGname(String gname) {
    this.gname = gname;
  }


  public Double getPrice() {
    return price;
  }

  public void setPrice(Double price) {
    this.price = price;
  }


  public Double getTraid() {
    return traid;
  }

  public void setTraid(Double traid) {
    this.traid = traid;
  }

}
